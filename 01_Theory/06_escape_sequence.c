#include <stdio.h>

int main()
{
    printf("Hello, World!\n"); // \n is an escape sequence for a new line
    printf("This is a tab:\tSee?\n"); // \t is an escape sequence for a tab
    printf("He said, \"C is fun!\"\n"); // \" is an escape sequence for a double quote
    printf("Backslash: \\\n"); // \\ is an escape sequence for a backslash
    return 0;
}